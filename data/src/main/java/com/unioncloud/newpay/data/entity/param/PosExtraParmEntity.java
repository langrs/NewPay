public class PosExtraParmEntity {
    private String parmId;
    private String parmNo;
    private String parmName;
    private String parmVal;

    public String getParmId() {
        return parmId;
    }

    public void setParmId(String parmId) {
        this.parmId = parmId;
    }

    public String getParmNo() {
        return parmNo;
    }

    public void setParmNo(String parmNo) {
        this.parmNo = parmNo;
    }

    public String getParmName() {
        return parmName;
    }

    public void setParmName(String parmName) {
        this.parmName = parmName;
    }

    public String getParmVal() {
        return parmVal;
    }

    public void setParmVal(String parmVal) {
        this.parmVal = parmVal;
    }
}
